/**
 * Render a PRStoryRich as a self-contained HTML page tailored for a
 * reviewer reading the PR file-by-file.
 * @param {import('../pr-build.js').PRStoryRich} story
 */
export function renderPrHtml(story) {
  const ctx = {
    branch: story.branch,
    github_base: story.github_base,
    repo_root: story.repo_root,
  };

  return `<!doctype html>
<html lang="en">
<head>
<meta charset="utf-8">
<meta name="viewport" content="width=device-width,initial-scale=1">
<title>${escapeHtml(`PR story — ${story.branch}`)}</title>
<style>${BASE_CSS}${PR_CSS}</style>
</head>
<body>
<header class="coding-agent-story-header">
  <h1>PR story <span class="pill">${escapeHtml(story.branch)}</span></h1>
  ${renderPrLink(story)}
  <span class="stats">${renderTotals(story.totals)}</span>
</header>
<main>
  ${renderBranchAndCommits(story)}
  ${renderOutcomesStrip(story.outcomes)}
  ${renderThrashSummary(story.files)}
  ${renderInterventionsAtTop(story.interventions)}
  <h2 class="section-h">Files in this PR (${story.files.length})</h2>
  ${story.files.length
    ? story.files.map((f, i) => renderFileSection(f, ctx, i === 0)).join('\n')
    : '<div class="empty">No file edits detected across the discovered sessions.</div>'}
  ${renderSessionsList(story)}
  <footer class="help-foot">Generated by coding-agent-story · all data stays on your machine.</footer>
</main>
</body>
</html>`;
}

