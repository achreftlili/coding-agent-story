function renderTotals(t) {
  const tk = t.tokens;
  const tokPill = tk ? `<span class="totals-pill" title="${escapeHtml(tokenTooltip(tk))}">${fmtTokens(tk.billable)} tokens</span>` : '';
  return `<span class="totals-pill">${t.sessions} sessions</span>` +
    `<span class="totals-pill">${t.edits} edits</span>` +
    `<span class="totals-pill">${t.decisions} decisions</span>` +
    `<span class="totals-pill">${t.interventions} interventions</span>` +
    tokPill;
}

function fmtTokens(n) {
  n = +n || 0;
  if (n === 0) return '—';
  if (n < 1000) return String(n);
  if (n < 1e6) return (n / 1000).toFixed(n < 10000 ? 1 : 0) + 'k';
  return (n / 1e6).toFixed(1) + 'M';
}

function tokenTooltip(t) {
  return `in: ${(t.input||0).toLocaleString()} · out: ${(t.output||0).toLocaleString()}` +
    ` · cache_read: ${(t.cache_read||0).toLocaleString()} · cache_create: ${(t.cache_creation||0).toLocaleString()}` +
    ` · billable: ${(t.billable||0).toLocaleString()}`;
}

function renderPrLink(story) {
  if (!story.pr) return `<span class="pr-link no-pr">no open PR detected</span>`;
  const state = story.pr.state ? ` (${story.pr.state.toLowerCase()})` : '';
  return `<a class="pr-link" href="${escapeHtml(story.pr.url)}" target="_blank" rel="noopener">#${story.pr.number}${escapeHtml(state)} ↗</a>`;
}

function renderBranchAndCommits(story) {
  const branchPill = renderBranchPill(story);
  const commitPills = (story.commits ?? []).slice(0, 10).map((c) => {
    const href = story.github_base ? `${story.github_base}/commit/${c.hash}` : null;
    const text = `<code>${escapeHtml(c.short)}</code> <span>${escapeHtml(oneLine(c.subject))}</span>`;
    return href
      ? `<a class="commit-pill" href="${escapeHtml(href)}" target="_blank" rel="noopener">${text}</a>`
      : `<span class="commit-pill">${text}</span>`;
  }).join('');
  const more = story.commits.length > 10 ? `<span class="commit-pill">+${story.commits.length - 10} more</span>` : '';
  return `${branchPill}<div class="commits">${commitPills}${more}</div>`;
}

function renderBranchPill(story) {
  const inside = `⎇ ${escapeHtml(story.branch)}`;
  if (story.github_base && story.branch && story.branch !== 'HEAD') {
    const href = `${story.github_base}/tree/${encodeURIComponent(story.branch)}`;
    return `<a class="branch-pill" href="${href}" target="_blank" rel="noopener" title="view branch on GitHub">${inside}</a>`;
  }
  return `<span class="branch-pill">${inside}</span>`;
}

function renderOutcomesStrip(outcomes) {
  const s = outcomes?.summary;
  if (!s) return '';
  if (!(s.tests_run || s.lints_run || s.builds_run)) return '';
  const testCls = s.tests_failed > 0 ? 'fail' : s.tests_passed > 0 ? 'pass' : '';
  return `<div class="outcomes-strip">
    ${s.tests_run ? `<span class="outcome-chip ${testCls}">🧪 ${s.tests_run} test runs · ${s.tests_passed} ✓ · ${s.tests_failed} ✗</span>` : ''}
    ${s.lints_run ? `<span class="outcome-chip">🪮 ${s.lints_run} lint runs</span>` : ''}
    ${s.builds_run ? `<span class="outcome-chip">🛠 ${s.builds_run} build runs</span>` : ''}
  </div>`;
}

function renderThrashSummary(files) {
  const hotspots = files.filter((f) => f.thrash.thrash > 0).slice(0, 8);
  if (!hotspots.length) return '';
  const rows = hotspots.map((f) =>
    `<tr><td>${escapeHtml(f.path)}</td><td class="num">${f.actions.length}</td><td class="num">${f.thrash.reverted}</td><td class="num">${f.thrash.changed_later}</td><td class="num">${f.thrash.thrash}</td></tr>`
  ).join('');
  return `<div class="thrash-summary">
    <h4>Thrash hotspots</h4>
    <table>
      <thead><tr><th>file</th><th class="num">edits</th><th class="num">reverted</th><th class="num">changed</th><th class="num">score</th></tr></thead>
      <tbody>${rows}</tbody>
    </table>
  </div>`;
}

function renderInterventionsAtTop(interventions) {
  if (!interventions.length) return '';
  const seen = new Set();
  const dedup = [];
  for (const i of interventions) {
    const k = i.raw_text.slice(0, 100);
    if (seen.has(k)) continue;
    seen.add(k);
    dedup.push(i);
  }
  const items = dedup.slice(0, 12).map((i) => {
    if (i.meta?.fork_text) {
      return `<div class="intervention qa">
        <div class="qa-label">Claude asked</div>
        <div class="qa-claude">${escapeHtml(i.meta.fork_text)}</div>
        <div class="qa-label">You said</div>
        <div class="qa-user">${escapeHtml(i.raw_text)}</div>
      </div>`;
    }
    return `<div class="intervention simple">${escapeHtml(i.raw_text)}</div>`;
  }).join('');
  return `<h2 class="section-h">Your calls (${dedup.length})</h2>${items}`;
}

function renderFileSection(f, ctx, openFirst) {
  const ghUrl = githubBlobUrl({
    base: ctx.github_base, branch: ctx.branch, repoRoot: ctx.repo_root, absFilePath: f.path,
  });
  const filename = `<a class="fp" href="${ghUrl ?? '#'}" ${ghUrl ? 'target="_blank" rel="noopener"' : ''}>${escapeHtml(f.path)}</a>`;
  const ac = f.alignment_counts;
  const badges = [];
  if (ac.kept)         badges.push(`<span class="badge-align kept">${ac.kept} kept</span>`);
  if (ac.reverted)     badges.push(`<span class="badge-align reverted">${ac.reverted} reverted</span>`);
  if (ac['changed-later']) badges.push(`<span class="badge-align changed-later">${ac['changed-later']} changed</span>`);
  if (ac['not-in-pr']) badges.push(`<span class="badge-align not-in-pr">${ac['not-in-pr']} not-in-PR</span>`);
  if (f.thrash.thrash > 0) badges.push(`<span class="badge-align reverted">thrash ${f.thrash.thrash}</span>`);

  const why = renderFileWhy(f);
  const edits = renderFileEdits(f, ctx);

  return `<details class="file-section"${openFirst ? ' open' : ''}>
    <summary>
      <span class="fp">${filename}</span>
      <span class="badges">${badges.join('')}</span>
      <span class="meta" style="margin-left:auto">${f.actions.length} action(s) · ${f.session_ids.length} session(s)</span>
    </summary>
    <div class="body">
      ${why}
      <h5>Edits in order</h5>
      ${edits}
    </div>
  </details>`;
}

function renderFileWhy(f) {
  if (!f.decisions.length && !f.interventions.length) return '';
  const decs = f.decisions.slice(0, 6).map((d) => `<li class="dec">${escapeHtml(d.raw_text)}</li>`).join('');
  const ints = f.interventions.slice(0, 6).map((i) => `<li class="int">📌 ${escapeHtml(i.raw_text)}</li>`).join('');
  return `<div class="why">
    ${decs ? `<h5>Why this file was touched</h5><ul>${decs}</ul>` : ''}
    ${ints ? `<h5>Your steering on this file</h5><ul>${ints}</ul>` : ''}
  </div>`;
}

function renderFileEdits(f, ctx) {
  const items = f.actions.map((a) => {
    const align = a._alignment ?? 'unknown';
    const alignBadge = `<span class="badge-align ${align}">${align}</span>`;
    const diff = renderActionDiff(a);
    const meta = a.meta ?? {};
    const stamp = a.timestamp ? `<span class="meta" style="margin-left:auto">${escapeHtml(a.timestamp.slice(0,16).replace('T',' '))}</span>` : '';
    const t = meta.turn_tokens;
    const tokChip = t
      ? `<span class="turn-tok" title="in: ${(t.input||0).toLocaleString()} · out: ${(t.output||0).toLocaleString()} · cache_read: ${(t.cache_read||0).toLocaleString()} · cache_create: ${(t.cache_creation||0).toLocaleString()} · billable: ${(t.billable||0).toLocaleString()}">⛁ ${fmtTokens(t.billable)}</span>`
      : '';
    return `<li class="action" style="display:flex; flex-direction:column;">
      <div style="display:flex; align-items:center; gap:6px; flex-wrap:wrap;">
        <span class="tool-name">${escapeHtml(meta.tool_name ?? 'Action')}</span>
        ${alignBadge}
        ${tokChip}
        ${stamp}
      </div>
      ${diff}
    </li>`;
  }).join('');
  return `<ul class="actions-under">${items}</ul>`;
}

function renderActionDiff(a) {
  const tool = a.meta?.tool_name;
  if (tool === 'Edit' || tool === 'MultiEdit') {
    const oldS = a.meta?.old_string ?? '';
    const newS = a.meta?.new_string ?? '';
    if (!oldS && !newS) return '';
    const lines = collapseContext(diffLines(oldS, newS), 2);
    return wrapDiff('diff', lines);
  }
  if (tool === 'Write') {
    const newS = a.meta?.new_string ?? '';
    if (!newS) return '';
    const lines = String(newS).split('\n').slice(0, 80).map((text) => ({ type: '+', text }));
    return wrapDiff('new file', lines);
  }
  if (tool === 'Bash') {
    return `<code class="tool-cmd">${escapeHtml(a.meta?.command ?? '')}</code>`;
  }
  return '';
}

function wrapDiff(label, lines) {
  const body = lines
    .map((d) => {
      const cls = d.type === '+' ? 'add' : d.type === '-' ? 'del' : 'ctx';
      const gutter = d.type === ' ' ? ' ' : d.type;
      return `<span class="ln ${cls}"><span class="ln-gutter">${gutter}</span>${escapeHtml(d.text)}</span>`;
    })
    .join('');
  return `<details class="diff"><summary>${escapeHtml(label)}</summary><pre>${body}</pre></details>`;
}

function renderSessionsList(story) {
  if (!story.session_ids.length) return '';
  return `<h2 class="section-h">Sessions in this PR (${story.session_ids.length})</h2>
    <div class="session-toc">${story.session_ids.map((id) => `<span><code>${escapeHtml(id.slice(0,8))}</code></span>`).join('')}</div>`;
}

function oneLine(s) {
  return String(s ?? '').replace(/\s+/g, ' ').trim();
}
